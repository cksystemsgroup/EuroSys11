											<p class="paragraph_style_2">Call for Workshops</p>											 
											<p class="paragraph_style_3">Researchers and practitioners are invited to submit proposals for
											workshops to be held in conjunction with the EuroSys 2011
											conference. The purpose of workshops is to provide a forum for
											exchanging opinions, presenting ideas, and discussing preliminary
											results in a small and interactive atmosphere.</p> 

											<p class="paragraph_style_3">Proposals should include: 
											<ul> 
											<li>Workshop name and a 150 to 200 word abstract describing the workshop, suitable for the conference web site.</li> 
											<li>A short summary of the topic, scope and significance of the
											workshop including a discussion on the relevance to EuroSys
											topics. Please include information about past iterations of the
											workshop, if any.</li> 
											<li>Information about the procedure for selecting papers, plans for
											dissemination (for example, special issues of journals, or position
											statements distributed to the participants), the expected number of
											participants, and the planned workshop format.</li> 
											<li>A brief CV of each of the organizers including contact addresses
											and an identification of a single primary contact.</li> 
											</ul> 
											</p> 

											<p class="paragraph_style_3">All proposals will be judged on their merits and the best proposals
											will be accepted. Specifically, workshops that have been organized at
											previous iterations of Eurosys are <em>not</em> automatically accepted
											this year. For instance, we will take into account the success of
											previous events (in terms of attendance, submissions, etc.) in our
											deliberations. </p> 




											<p class="paragraph_style_2">Responsibilities</p> 
											<p class="paragraph_style_3">The EuroSys organization provides: 
											<ul> 
											<li>Link from EuroSys and Workshop web sites.</li> 
											<li>Management of registration for participants.</li> 
											<li>Setup of meeting space, and related equipment.</li> 
											<li>Coffee-breaks and workshop lunch arrangements.</li> 
											</ul> 
											</p> 

											<p class="paragraph_style_3">For workshops interested in publishing their proceedings, the
											EuroSys organization will provide the following support. Note that
											workshop proceedings would not be part of the EuroSys conference
											proceedings, but would be published as a separate volume.
											<ul> 

											<li>Collect camera-ready versions of the papers and their copyright forms.</li> 
											<li>Provide support for publishing on the on-site CD.</li> 
											<li>Provide support to obtain an ISBN number.</li> 
											<li>Publish papers in ACM digital library (pending confirmation).</li> 
											</ul> 
											</p> 

											<p class="paragraph_style_3">The main responsibility of organizing a workshop goes to the chairperson(s) of the workshop. The workshop organization must take care of:
											<ul> 
											<li>Coordinating deadlines and acceptance notifications for workshop papers with the EuroSys Workshop co-chairs.</li> 
											<li>Workshop publicity, call for papers, and submission and review process.</li> 

											<li>Providing a brief description of the workshop for inclusion on the EuroSys web page.</li> 
											<li>Scheduling workshop activities in collaboration with the EuroSys local organizers.</li> 
											</ul> 
											</p> 

