											<p style="padding-top: 0pt; " class="paragraph_style_2">EuroSys 2011 Conference<br /></p>
											<p class="paragraph_style_3"><b>The EuroSys 2012 page is online and can be found <a href="http://eurosys2012.unibe.ch">here</a>.</b> <br><br>EuroSys 2011 is organized by <a href="http://www.eurosys.org/">EuroSys</a>, the European Chapter of SIGOPS, sponsored by <a href="http://www.sigops.org/">ACM SIGOPS</a>. <br /><br />

											The EuroSys conference series brings together professionals from academia and industry. It has a strong focus on systems research and development: operating systems, data base systems, real-time systems and middleware for networked, distributed, parallel, or embedded computing systems. EuroSys has become a premier forum for discussing various issues of systems software research and development, including implications related to hardware and applications. 
<br /><br />
											EuroSys 2011 will follow the pattern established by the previous EuroSys conferences, by seeking papers on all aspects of computer systems. EuroSys 2011 will also include a number of workshops to allow junior and senior members of the systems community to explore leading-edge topics and ideas before they are presented at a conference.</p>

