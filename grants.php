											<p class="paragraph_style_2">Travel Grants</p>
											<p class="paragraph_style_3">EuroSys, with generous donations from our sponsors <a href="http://www.eurosys.org/" target="_blank">Eurosys</a>,  <a href="http://www.artist-embedded.org" target="_blank">Artist Design</a>, and <a href="http://www.sbg.ac.at" target="_blank">University of Salzburg</a>, offers several grants to enable students, post-docs, and faculty, worldwide to attend the conference. Students, post-docs and researchers in Eastern Europe are specially invited to apply. The grants cover the EuroSys conference registration, including meals and proceedings, and possibly hotel room during the conference and funding for travel expenses.</p>
<p class="paragraph_style_3">
Grants will be awarded by the grant committee based on a combination of need and merit. Students receiving a travel grant need to agree to serve as a session scribe (if required) during the conference. Applicants are encouraged to participate in the poster session and in the workshops.</p>

<p class="paragraph_style_2">Who can apply?</p> 
 
<p class="paragraph_style_3">Funding is available to all students, post-docs and faculty at educational institutions, world-wide. Besides students, post-docs and researchers in Eastern Europe are also invited to apply.</p> 
 
<p class="paragraph_style_2">How to apply</p> 
 
<p class="paragraph_style_3">To apply you should submit the following:
<ul> 
<li>A fully filled-out EuroSys Travel Grant Application. The
application form appears below.</li> 
<li>Students:
<ul><li>A letter of recommendation from your advisor, supporting the
application, which must cover the technical merit of your
participation. </li> 
</ul></li> 
<li>A short position paper written by you explaining what benefit is
expected from attending the conference and why the scholarship should
be granted. The statement should describe any research you have been
doing and list any papers you have published. It should be no longer
than one page.</li> 
</ul> 
</p> 
 
<p class="paragraph_style_3">Applications have to be in plain text (no attachments, simple ASCII text in the body of your message), in English, and submitted by e-mail to <a href="mailto:scholarships@eurosys.org">scholarships@eurosys.org</a>. The advisor's statement should be sent separately from the student's application, and should indicate the student's name in the "Subject" line.</p> 
 
<p class="paragraph_style_3">The deadline for receipt of applications for student scholarships is March 1st, 2011.</p> 
 
<p class="paragraph_style_3">Notifications will be sent out via e-mail by March 14th, 2011 .</p> 
 
<p class="paragraph_style_3">For more information, contact Yolande Berbers (<a href="mailto:Yolande.Berbers@cs.kuleuven.ac.be">Yolande.Berbers@cs.kuleuven.ac.be</a>) or Dejan Kostic (<a href="mailto:dejan.kostic@epfl.ch">dejan.kostic@epfl.ch</a>).</p> 
 
