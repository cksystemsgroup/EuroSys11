											<p class="paragraph_style_3"><b>Data Deduplication - Algorithms and Implementations</b></p>
											<p class="paragraph_style_3">Andre Brinkmann (University of Paderborn)<br><br>
											This tutorial presents and discusses the different approaches laying behind today’s data deduplication environments. Data deduplication systems discover redundancies between different data blocks and remove these redundancies to reduce storage capacity. Data deduplication has become a hot topic in both academia and industry, as it promises a huge reduction of the required backend storage capacity.</p>	

