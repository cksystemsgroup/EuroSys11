                                                                                        <p class="paragraph_style_3"><b>Distributed coordination using ZooKeeper</b></p>
                                                                                        <p class="paragraph_style_3">Flavio Junqueira (Yahoo! Research), Benjamin Reed (Yahoo! Research)<br><br>
                                                                                        In this tutorial, we ﬁrst review the basic challenges of distributed systems
from both a theoretical and real world perspective to motivate the need for a
coordination system. We introduce ZooKeeper and show with examples how
one can use it to simplify the design of a distributed system. Even though
ZooKeeper enables the system developer to deal with challenges of distributed
systems eﬀectively and eﬃciently, it does not eliminate them completely. We
also review some of the corner cases that designers and developers still need to
deal with. We will also share some of the pitfalls that others have fallen into,
and explain why some obvious new features, such as watches with information
about changes, are bad ideas. We will conclude by sharing upcoming changes,
desired features, and ZooKeeper related research directions.</p>

