																		<p class="paragraph_style_2">Call for Posters and Demos  <a href="Downloads/CFP_EuroSys2011_Posters.pdf" target="_blank"><img src="Images/pdf.png" width = "32px" border="0" /></a><a href="Downloads/CFP_EuroSys2011_Posters.txt" target="_blank"><img src="Images/txt.png" width = "32px" border="0" /></a></p>
																		<p class="paragraph_style_3">
We invite EuroSys attendees to participate in a special poster session that will foster lively, informal and in-depth discussions. EuroSys Posters &amp; Demos will be selected in advance on the basis of two-page PDF abstracts, with fonts no smaller than 10 point. Abstracts must be submitted by sending email to: <a href="mailto:eurosys-2011-posters@googlegroups.com">eurosys-2011-posters@googlegroups.com</a> 
by  March 1, 2011, 23:59 UTC.
</p>

<p class="paragraph_style_3">
Posters will be selected on the following basis:
<ul>
<li>Submissions must describe new, interesting ideas on any EuroSys area such as operating systems or distributed systems. Posters may be accompanied by demos.</li>
<li>Submissions should present early work that is not yet submitted to a refereed conference or journal. Inclusion of early experimental results is particularly encouraged.</li>
<li>Student submissions meeting the above criteria will be given preference; however, non-students may also submit abstracts.</li>
</ul>
</p>

<p class="paragraph_style_3">
Please provide the following information in your PDF file:
<ul>
<li>Poster title.</li>
<li>Author names, affiliations, and email addresses.</li>
<li>Note which authors, if any, are students.</li>
<li>Indicate if you plan to set up a demo with your poster.</li>
</ul>
</p>

<p class="paragraph_style_3">
Authors will be notified of acceptance or rejection via e-mail by March 8, 2011. No reviews will be provided. Posters will be published online at the conference Web site. The poster session is scheduled for 1:30 pm Tuesday, April 12. At least one author should register to the conference and present the poster throughout the entire 90 minutes of the session. Authors of the five best posters will be given 3 minutes each to present their work before the poster session.
</p>

<p class="paragraph_style_3">
Please send any questions about the posters &amp; demos session to: <a href="mailto:eurosys-2011-posters@googlegroups.com">eurosys-2011-posters@googlegroups.com</a>
</p>



											
